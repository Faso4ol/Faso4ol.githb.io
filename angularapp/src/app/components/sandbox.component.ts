import { Component } from '@angular/core';

@Component({

    selector: 'sandbox',
    template: `
    
    <div class="container" style ="margin-bottom: 50px;">
    <div class="row">
    <div class="col-md-6 offset-md-3" style = "text-align: center">

    <img src="assets/mega-travel.jpg" class="rounded" alt="logo" width= 300>

    </div>
    </div>
    <div class="row">
    <div class="col-md-6 offset-md-3">
        <form>
            <div class="form-group">
                <label>Select Destination</label>
                <select (change) = "onSelect()" class="form-control" [(ngModel)]= "destination" name="destination">
                    <option>Brisbane, Australia</option>
                    <option>Vancouver, Canada</option>
                    <option>New York City, United States</option>
                    <option>Berlin, Germany</option>
                    <option>Cancun, Mexico</option>
                </select>
            </div>

            <div class="form-group">

                <label>Activities</label>
                
                <input disabled type="text" class="form-control" [(ngModel)]="activities" name="activities"/>     

            </div>

            <div class="form-group">

                <label>No. of Adults</label>
                <input type="number" class="form-control" [(ngModel)]="noOfAdults" name="noOfAdults">

            </div>

            <div class="form-group">

                <label>No. of Children</label>
                <input type="number" class="form-control" [(ngModel)]="noOfChildern" name="noOfChildern">

            </div>

            <div class="form-group">

            <label>Travel Date</label>
                <input type="date" class="form-control" [(ngModel)]="date" name="date">
            </div>

            <div class="form-group">

            <label>Name</label>
                <input type="text" class="form-control" [(ngModel)]="name" name="name">
            
            </div>

            <div class="form-group">

            <label>Contact#</label>
                <input type="text" class="form-control" [(ngModel)]="contact" name="contact">
            
            </div>

            <div class="form-group">

            <label>Email</label>
                <input type="email" class="form-control" [(ngModel)]="email" name="email">
            
            </div>
            <div class="form-group">
            <input type="submit" (click) = "onSubmit()" class="btn-success btn btn-secondary" value="Submit">
            </div>

            <div class="alert alert-success"  [style.display]="getStyle()"style="display:none" role="alert"> 
         {{msg}}
      </div>

            

        </form>
    </div>
    </div>
    </div>
    `
})

export class SandboxComponent {

    name:string = "";
    email:string = "";
    contact:string = "";
    destination:string ="";
    date:string = "";
    activities:any = "";
    noOfChildern:number = 0;
    noOfAdults:number = 0;     
    msg:string = "";   
    display:boolean = false;      
    
    getStyle(){
        if(this.display){
            return 'block';
 
        }else{
            return 'none';
        }
    }

    onSelect(){

        if(this.destination == "Brisbane, Australia")
        {
            this.activities = "Brisbane	City Tours, Sports, Cycling, Museums, Boating";
        }
        else if(this.destination == "Vancouver, Canada")
        {
            this.activities = "Museums, Sailing, Beach, Hiking, Boating";
        }
        else if(this.destination == "New York City, United States")
        {
            this.activities = "Museums, Theatre, Parks and Recreation, City Tours";
        }
        else if(this.destination == "Berlin, Germany")
        {
            this.activities = "City Tours, Museums, Cycling";
        }
        else if(this.destination == "Cancun, Mexico")
        {
            this.activities = "Parks and Recreation, Beaches, Boating, Snorkeling";
        }

    }
    onSubmit(){
       this.msg = 'Thank you for submiting!';
       this.display = true; 
    }

}